package com.javapro;

/**
 * @AccessSpecifiers can be only
 * 
 *                   public abstract
 * 
 * @IndustryStandard naming convention App
 * 
 *                   we can have @CamelCaseLetters also
 */
public class App {
	/**
	 * 
	 * if @Main method does not exist it will not execute but will get compiled!
	 * we can @OverLoad the main method
	 * 
	 * @public & @static because O.S
	 */
	public static void main(String[] args) {
		
		/**
		 * @System
		 */
		System.out.println("Hello World!");
	}
}